    <section id="section-history">
      <h1>История техникума</h1>
      >
      <div class="section-history__content-block">
        <div class="imgs">
          <img src="img/history3.jpg" alt="history" width="400" />
          <img src="img/history2.png" alt="history" width="400" />
          <img src="img/history.jpg" alt="history" width="400" />
        </div>
        <p>
          В октябре 1929 года в системе "Росстроя" на основании приказа ВСНХ
          СССР № 1276 от 25 сентября 1929 года был организован строительный
          техникум - одно из старейших учебных заведений среднего
          профессионального образования Тульской области.<br /><br />

          Годом позже техникум получил старинный особняк в центре города, бывший
          в свое время губернской Казенной палатой, в которой служил М.Е.
          Салтыков-Щедрин. Преподаватели и студенты собственными силами вечерами
          после занятий надстроили здание двумя этажами, и в 1932 году оно
          приняло свой нынешний облик. В те годы здесь готовили специалистов в
          области строительства и эксплуатации зданий и сооружений. Эта
          специальность 270103 «Строительство и эксплуатация зданий и
          сооружений» - старейшая в техникуме - существует и поныне.<br /><br />

          В годы войны обучение проводилось в другом здании, так как учебный
          корпус переоборудовали под госпиталь. В период обороны Тулы
          (ноябрь-декабрь 1941 года) по центральной улице города ходили трамваи,
          на которых после ожесточенных боев, проводимых в 2-3 км от техникума,
          привозили раненых бойцов. Несмотря на тяжелые военные условия именно
          тогда в техникуме была открыта еще одна специальность - 270110 "Монтаж
          и эксплуатация сантехнических устройств и вентиляции", а в
          послевоенные годы, когда страна остро нуждалась в специалистах,
          способных восстановить разрушенные войной мосты и дороги, техникум
          начал обучение студентов по специальности 270207 "Строительство и
          эксплуатация городских путей сообщения". В 1949 году было открыто
          вечерние отделение, в 1954 году - заочное.<br /><br />

          С первых дней существования техникума в нем работали
          высококвалифицированные преподаватели, которые были направлены из
          высших учебных заведений.<br /><br />

          Большую роль в становлении и развитии техникума сыграли директора,
          каждый из которых внес свою лепту в профессиональное образование
          города Тулы и нашей страны.
        </p>
